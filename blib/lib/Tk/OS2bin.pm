package Tk::OS2bin;
$VERSION = 0.2;
1;

=head1 NAME

Tk::OS2bin - deliver the binary distribution of Tk/Perl via CPAN.

=head1 SYNOPSYS

  perl -MCPAN -e "test 'Tk::OS2bin' "
  perl -MCPAN -e "install 'Tk::OS2bin' "

or

  perl -MCPAN -e shell
  CPAN> test Tk::OS2bin
  CPAN> install Tk::OS2bin

No additional software but a complete correctly installed Perl binary
distribution (and, of course EMX and OS/2) is required.

=cut
