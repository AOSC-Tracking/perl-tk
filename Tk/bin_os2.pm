package Tk::bin_os2;
$VERSION = '0.3';
1;

=head1 NAME

Tk::bin_os2 - deliver the binary distribution version 0.3 of
module Tk version 800.012 for os2 and Perl version 5.00553
via CPAN.

=head1 SYNOPSYS

  perl -MCPAN -e "test 'Tk::bin_os2' "
  perl -MCPAN -e "install 'Tk::bin_os2' "

or

  perl -MCPAN -e shell
  CPAN> test Tk::bin_os2
  CPAN> install Tk::bin_os2

No additional software but a complete correctly installed Perl (binary)
distribution is required.

=head1 SEE ALSO

L<Tk>, L<CPAN>, L<perl>.

=cut
